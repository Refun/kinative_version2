                     <h2 class="blog-heading margin-t35">Front End Designer<mark class="red">.</mark></h2>
                     <br>

                    <h3 class="blog-points">Pros and cons</h3>
                    
                    <p class="blog-para max-738">
                      Frank to say I am very much delighted to hear the emerging term ‘Front-end-Design’ .So, what is front end design exactly. Frontend design involves creating the HTML, CSS, and presentational JavaScript code that makes up a user interface.” Front end designer has the duty of maintaining a pixel perfect experience in the user interfaces . An user interface designer will hand over the design to him and you do look after it.
                       <img class="margin-t50 img-responsive " src="images/frontend_blog3.jpg" alt="blog-image"/>

                       <p class="text-center" style="margin-top: 10px"> Photo credit: pixel.com</p>
                       <h3 class="blog-points">Main Responsiblities:</h3>
                      <p class="blog-para max-738">01. They understand UX principles and best practices, but may not spend their time conducting research, creating flows, and planning scenarios.</p>
                      <p class="blog-para max-738">02. They have a keen eye for aesthetics, but may not spend their time pouring over font pairings, comparing color palettes, or creating illustrations and icons.</p>
                       <p class="blog-para max-738">03. Efficiency in writing semantic coding approach.</p>
                      <p class="blog-para max-738">03. Profound and Solid understaning of responsive design and able to implement it through different devices like mobiles,tablets and Desktops.</p>
                       <p class="blog-para max-738">04. Master moduler CSS or use a CSS pre processors like LESS, SCSS, Stylus and so on.</p>
                       <p class="blog-para max-738">05. Workalbe knowledge of Photoshop or sketch or may be Invision to understand, extract images and collaborate on designs provided by the designers.</p>
                      <p class="blog-para max-738">06. They can write JavaScript, but may not spend their time writing application-level code, wiring up middleware, or debugging.</p>
                      <p class="blog-para max-738">07. They understand the importance of backend development, but may not spend their time writing backend logic, spinning up servers, load testing, etc.</p>
                    </p>
                     
                      <blockquote class="qoute">

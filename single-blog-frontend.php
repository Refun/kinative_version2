                     <h2 class="blog-heading margin-t35">Front-End-Engineer<mark class="red">.</mark></h2>
                    <h3 class="blog-points">Pros and cons</h3>
                    <p class="blog-para max-738">

                       Here comes the most anticipated term ‘Front-End-Engineer’.What the heck the term ‘engineer’ doing in the front end development industry. Let’s make it clear. Actually this is not very long time , when the term "front-end engineer" has coined for the first time in web development industry.With the emergence of javascript in almost everywhere (webapp,mobile development,embedded devices) a developer has to think in a very profound way to extract any solution.'MEAN STACK' also accelerates the enginnering approach in the front end part. Companies like Facebook,Google,Linkedin are using robust front end technologies in their platforms to serve better user experience and perfromance.In a word it’s the engineering approach in the development world of Front-End part. With the emergence of libraries and frameworks like React, Angular the Front end part has become so much extensive and rich. It’s almost difficult for one person to handle all the activities with a single hand also.
                      A modern engineering approach of web development differs from typical development process in many ways. Firstly, with the development environment and then page loading and performance issues. By the blessings of NODE.js and it’s package manager NPM, a modern front-end engineer now has to deal with CLI (Command line interface) for using build task commands. Now there are also css technologies like Sass and Less and also Html templating engine like Jade, Embed Js,Handlebar.js etc which needs a lot of technical implementation of your knowledge.Here are some requirements for a Front-End-Engineer to fulfill.
                       <img class="margin-t50 img-responsive " src="images/frontend_blog2.jpg" alt="blog-image"/>

                       <p class="text-center" style="margin-top: 10px"> Photo credit: pixel.com</p>
                      <h3 class="blog-points">Main Responsiblities:</h3>
                      <p class="blog-para max-738">
                      <p class="blog-para max-738">01. Manitains performance and automation of your applications by using top notch technologies like React,Angualar or Vue. Know your main front-end engine “Browsers”. Learn how browser’s load and render assets including browser engine structure.</p> 
                      <p class="blog-para max-738">02. Have a proper understanind of RAW Javascript before learning jQuery — a lot of newbies directly jump into jQuery without learning the main programming concepts and Javascript. It is ok for a front end designer or may be for some Front-End-Developer(not all) but certainly not for a Front-End-Engineer. 
                      <p class="blog-para max-738">03. Gain knowledge of client- server architecture and network performance evaluation to see the assets loading to your page using chrome or firefox developer tools or your favorite developer tools
                       </p> 
                      <p class="blog-para max-738">04. Master CLI or terminal.Believe me, when you are going to work with build or automation process for your project you gonna need some command line skills and understandings.</p>
                      <p class="blog-para max-738">05. Master any of the one automation/build tools like Grunt ,Gulp or Webpack — about how to build your package to minify/uglify your CSS and JS files, cleaning extra markups and style classes in production version of your page.</p>
                      <p class="blog-para max-738">06. Lean how to use GIT version controlling using Github. Even if you are working alone in a project using Git and Github is a top notch professional approach and your employer would definitely give an extra consideration if you have intermediate-advanced Git skills.</p>
                      <p class="blog-para max-738">07. Try to deploy your project on cloud platform like Heroku and learn how to directly deploy projects by connecting Github and Cloud platform using CLI.
                     </p>
                    </p>


                    <h2 class="blog-heading margin-t35">Front-End-Development<mark class="red">.</mark></h2>
                    <h3 class="blog-points">Summary:</h3>
                    <p class="blog-para max-738">
                      “Front-End-Developer” is the most cherished one and also one of the most widely known term in the web world for a long time. I have lived on this term for all of career. A website has a physical 2 ends which are front-end and back-end. So you can asily assume all the responsiblities in the front end part that are maintained efficiently by a front end developer.The opportunity is huge for a front end developer as the 'MEAN Stack' is getting popular day by day. It means code sharing and also writing one language(Javascript) on front and back to take advantage.No doubt specialization is the demand of the industry and people are coping up the trends and it's going to brust out in the coming days!!!
                     
                    </p>
                 

                    
                                   
                    
                 
                     

